package com.AQuality.core;

public class Credentials {

    private String airVisualApiKey;
    private String discordBotToken;
    private String googleMapsApiKey;

    public String getAirVisualApiKey() {
        return airVisualApiKey;
    }

    public void setAirVisualApiKey(String airVisualApiKey) {
        this.airVisualApiKey = airVisualApiKey;
    }

    public String getDiscordBotToken() {
        return discordBotToken;
    }

    public void setDiscordBotToken(String discordBotToken) {
        this.discordBotToken = discordBotToken;
    }

    public String getGoogleMapsApiKey() {
        return googleMapsApiKey;
    }

    public void setGoogleMapsApiKey(String googleMapsApiKey) {
        this.googleMapsApiKey = googleMapsApiKey;
    }
}